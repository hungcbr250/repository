    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int in = tblCH.getSelectedRow();
        int j = tblCH.getSelectedRowCount()-1 ;
        
        int chon = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (chon == JOptionPane.YES_OPTION) {
            if (in >= 0) {
                for (int i = in; i <= in + j; i++) {
                    service.xoaCH(tblCH.getValueAt(in, 0).toString());
                    Fill();
                }
                JOptionPane.showMessageDialog(this, "Xóa thành công");

            } else {
                JOptionPane.showMessageDialog(this, "Mời chọn cửa hàng cần xóa");
            }
        } else {
            return;
        }
    }//GEN-LAST:event_btnXoaActionPerformed

