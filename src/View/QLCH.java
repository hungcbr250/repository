    public void Fill() {
        tableModel = (DefaultTableModel) tblCH.getModel();
        listCH = service.getListCH();
        tableModel.setRowCount(0);
        for (CH_VMD ch : listCH) {
            tableModel.addRow(new Object[]{ch.getMa(), ch.getTen(),
                ch.getDiaChi(), ch.getThanhPho(), ch.getQuocGia(),
                ch.getDiaChi() + " " + ch.getThanhPho() + " " + ch.getQuocGia()});

        }
    }

    public boolean check() {
        if (txtMa.getText().isBlank()==true) {
            lblMa.setText("Chống");

        } else {
            lblMa.setText("");
        }
        if (txtTenCH.getText().isBlank()==true) {
            lblten.setText("Chống");
        } else {
            lblten.setText("");
        }
        if (txtDC.getText().isBlank()==true) {
            lblDC.setText("Chống");
        } else {
            lblDC.setText("");
        }
        if (txtTP.getText().isBlank()==true) {
            lblTP.setText("Chống");
        } else {
            lblTP.setText("");
        }
        if (!txtQG.getText().isBlank()==true) {
            lblQG.setText("");
        } else {
            lblQG.setText("Chống");
            return false;
        }

        return true;

    }
    

    public boolean checkMa() {
        for (CH_VMD ch : listCH) {
            if (ch.getMa().equalsIgnoreCase(txtMa.getText())) {
                lblMa.setText("Trùng mã");
                return false;
            }
        }
        return true;
    }

